    <small>Art. 27 de la Constitution Belge</small>   
    <p>Les Belges ont le droit de s'associer; ce droit ne peut être soumis à aucune mesure préventive.</p> 
    <a href="http://www.senate.be/doc/const_fr.html">Source</a>
    <p>Vous êtes intéressés par le monde associatif ou simplement curieux de savoir comment créer votre association de collectionneurs de timbres ? Ce site se veut être une porte d’entrée pour vous familiariser avec les grandes règles que vous devez impérativement connaître pour créer et gérer votre association.</p>
    <p>Nous vous présentons donc une approche limitée de la loi sur les associations sans but lucratif (asbl) ainsi que de ce qu’est une asbl, de son mode de fonctionnement afin de vous aider à répondre aux questions de savoir comment créer une asbl et comment la gérer. Vous trouverez quelques sources pour compléter votre information dans les rubriques des liens.</p> 
    <p>Assoc.be n'a pas pour vocation de fournir des informations administratives ou juridiques personnalisées. 

Je l'ai crée tout simplement pour avoir une source d'information personnelle qui est aussi utile pour d'autres. 

Nous vous invitons à faire appel aux diverses agences conseil dont certaines vous sont proposées dans la rubrique des liens. </p>
