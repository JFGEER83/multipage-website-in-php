    <h3 class="section-title-secondary">plus d'infos?</h3>
La loi qui régit les ASBL (Association Sans But Lucratif), est la loi du 27 juin 1921 qui a été modifiée par la loi du 2 mai 2002.

<p>Plusieurs Arrêtés royaux et Arrêtés ministériels, disponibles sur le site Internet du SPF Justice, ont également été promulgués concernant la loi sur les ASBL.
www.just.fgov.be
    <span><a href="http://www.ejustice.just.fgov.be/tsv_pub/index_f.htm"></a></span>
</p> 


<p>Vous trouverez une bonne brochure explicative sur :
     <span><a href="https://justice.belgium.be/fr/publications/vzw_s"></a></span>
</p> 


<p>La loi prévoit également les obligations à respecter en ce qui concerne la création, le fonctionnement et la dissolution de l’ASBL.</p> 

