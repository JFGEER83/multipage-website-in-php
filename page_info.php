<div class="col-12 mx-auto">
    <h3 id="ancre-formalites" class="section-title-secondary">Les formalités</h3>
    <h4 class="section-title-third">Etapes</h4>
    <p>Deux étapes sont nécessaires à la constitution de votre ASBL (Association Sans But Lucratif):</p>
    <ul>
        <li><i class="far fa-square"></i>Dépôt des statuts et du registre des membres au greffe du tribunal de commerce</li>
        <li><i class="far fa-square"></i>Publication des statuts et des administrateurs</li>
    </ul>
    <p>Les frais de publication sont payables par chèque, mandat postale libellé au nom du Moniteur belge, ou par virement bancaire préalable ou versement au profit du compte du Moniteur belge.</p>
    <p id="ancre">Pour réaliser cette formalité vous disposez d’un formulaire intitulé :
« de demande d’immatriculation (BCE) et/ou de publication dans les annexes du Moniteur belge »
composé de trois volets :</p>
    <ul>
        <li><i class="far fa-square"></i><strong>volet A :</strong>  Identification ;</li>
        <li><i class="far fa-square"></i><strong>volet B :</strong> Copie à publier aux annexes du Moniteur belge après dépôt de l’acte;</li>
        <li><i class="far fa-square"></i><strong>volet C :</strong>Données supplémentaires à compléter lors d’un premier dépôt d’une personne morale.</li>
    </ul>
