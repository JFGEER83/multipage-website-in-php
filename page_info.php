        <div class="col-12 col-sm-6">
            <h2 class="section-title-third">ATTENTION!!</h2>
            <p>Le salarié peut être membre de l'ASBL, si il est membre effectif, il faudra distinguer clairement ses rôles et fonctions.</p>
            <p>Pour rappel, les administrateurs sont nommés par l'Assemblée Générale. ( Art. 4. de la loi du 27 juin 1921 modifiée par l’Art. 12. de la loi du 2 mai 2002.)</p>
            <h3>Comme pour un employeur classique, le salarié devra être déclaré à la Sécurité Sociale, aux Allocations Familiales, aux Vacances Annuelles.</h3>
        </div>
        <div class="col-12 col-sm-6">
            <h2 class="section-title-third">LA RETRIBUTION DES BENEVOLES ET DES ADMINISTRATEURS</h2>
            <p>Une ASBL peut rétribuer un administrateur sans que la rétribution ne soit excessive.
                L'administrateur peut par ailleurs être rétribué comme volontaire.</p>
            <p>Pour les volontaires, des régles précises existent quant aux défraiements : </p>
            <ul>
            <li><i class="far fa-square"></i><a href="http://www.volontariat.be/indemnites-ou-defraiement-des-benevoles">Indemnités ou défraiement des bénévoles</a></li>
            </ul>
            <p>Pour rappel, un travailleur de l'ASBL ne peut pas y être engagé comme bénévole.</p>
